export async function exportSalesReport(reportData: any) {
    const { sales, totalRevenue, totalSales, totalItems, byPayment } = reportData

    const paymentSummary = Object.entries(byPayment)
        .map(([method, amt]: [string, any]) => `${method}: ₹${amt.toLocaleString()}`)
        .join('  |  ')

    const doc = new Document({
        sections: [{
            properties: {
                page: {
                    margin: { top: 720, right: 720, bottom: 720, left: 720 },
                    pageNumbers: { start: 1 },
                },
            },
            headers: { default: brandHeader() },
            footers: { default: brandFooter() },
            children: [
                ...titleBlock('Sales Report', 'Revenue & Transaction Analysis'),
                summaryRow([
                    { label: 'Total Revenue', value: `₹${totalRevenue.toLocaleString()}` },
                    { label: 'Transactions', value: String(totalSales) },
                    { label: 'Items Sold', value: String(totalItems) },
                    { label: 'Payment Split', value: paymentSummary.length > 30 ? `${Object.keys(byPayment).length} methods` : paymentSummary },
                ]),
                sectionHeading('Transaction Details'),
                dataTable(
                    ['Date', 'Customer', 'Items', 'Payment', 'Discount', 'Total'],
                    sales.map((s: any) => [
                        new Date(s.created_at).toLocaleDateString('en-IN'),
                        s.customer_name || 'Walk-in',
                        String(s.sale_items?.length ?? 0),
                        s.payment_method,
                        `₹${Number(s.discount_amount ?? 0).toLocaleString()}`,
                        `₹${Number(s.total_amount).toLocaleString()}`,
                    ])
                ),
            ],
        }],
    })

    const blob = await Packer.toBlob(doc)
    saveAs(blob, `Virudti_Sales_Report_${new Date().toISOString().slice(0, 10)}.docx`)
}

export async function exportStockReport(reportData: any) {
    const { items, totalStock, totalValue, lowStockCount, outOfStockCount } = reportData

    const doc = new Document({
        sections: [{
            properties: {
                page: {
                    margin: { top: 720, right: 720, bottom: 720, left: 720 },
                    pageNumbers: { start: 1 },
                },
            },
            headers: { default: brandHeader() },
            footers: { default: brandFooter() },
            children: [
                ...titleBlock('Stock Report', 'Inventory Levels & Valuation'),
                summaryRow([
                    { label: 'Total Units', value: totalStock.toLocaleString() },
                    { label: 'Total Value', value: `₹${totalValue.toLocaleString()}` },
                    { label: 'Low Stock', value: String(lowStockCount) },
                    { label: 'Out of Stock', value: String(outOfStockCount) },
                ]),
                sectionHeading('Inventory Breakdown'),
                dataTable(
                    ['Product', 'Branch', 'SKU', 'Stock', 'Price', 'Value'],
                    items.map((i: any) => [
                        i.product?.name ?? '—',
                        i.branch?.name ?? '—',
                        i.sku ?? '—',
                        String(i.stock_count),
                        `₹${Number(i.selling_price ?? 0).toLocaleString()}`,
                        `₹${(i.stock_count * Number(i.selling_price ?? 0)).toLocaleString()}`,
                    ])
                ),
            ],
        }],
    })

    const blob = await Packer.toBlob(doc)
    saveAs(blob, `Virudti_Stock_Report_${new Date().toISOString().slice(0, 10)}.docx`)
}

export async function exportTrendsReport(reportData: any) {
    const { entries, totalMovements, adds, removes } = reportData

    const doc = new Document({
        sections: [{
            properties: {
                page: {
                    margin: { top: 720, right: 720, bottom: 720, left: 720 },
                    pageNumbers: { start: 1 },
                },
            },
            headers: { default: brandHeader() },
            footers: { default: brandFooter() },
            children: [
                ...titleBlock('Trend Analysis', 'Stock Movements Over Time'),
                summaryRow([
                    { label: 'Total Movements', value: String(totalMovements) },
                    { label: 'Stock Added', value: String(adds) },
                    { label: 'Stock Removed', value: String(removes) },
                    { label: 'Net Change', value: String(adds - removes) },
                ]),
                sectionHeading('Movement History'),
                dataTable(
                    ['Date', 'Product', 'Action', 'Previous', 'New', 'Reason'],
                    entries.map((e: any) => [
                        new Date(e.created_at).toLocaleDateString('en-IN'),
                        e.product?.name ?? '—',
                        e.action_type,
                        String(e.old_count),
                        String(e.new_count),
                        e.reason ?? '—',
                    ])
                ),
            ],
        }],
    })

    const blob = await Packer.toBlob(doc)
    saveAs(blob, `Virudti_Trend_Analysis_${new Date().toISOString().slice(0, 10)}.docx`)
}
